package Array_Programs;

public class Even_Numbers {
	
	public static void main(String[] args) {
		
		// Programme for Even_Numbers
		
		int x = 2;
		
		if(x%2==0)
		{
			System.out.println(x + " is the even number");
		}
		else
		{
			System.out.println(x + " is the odd number");
		}
		
		
	}

}
