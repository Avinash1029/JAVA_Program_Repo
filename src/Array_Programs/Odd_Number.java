package Array_Programs;

public class Odd_Number {

	public static void main(String[] args) {
		
		// Programme for Odd_Numbers //
		
		int x = 3;
		
		if(x%2==0)
		{
			System.out.println(x + " is the even number");
		}
		else
		{
			System.out.println(x + " is the odd number");
		}

	}

}
